export class UserData {
    uId: number
    uName: string
    uEmail: string
    uContact: number
    uAddress: string
    uPassword: string

    constructor(uId: any, uName: any, uEmail: any, uContact: any, uAddress: any, uPassword: any) {
      this.uId = uId
      this.uName = uName
      this.uEmail = uEmail
      this.uContact = uContact
      this.uAddress = uAddress
      this.uPassword = uPassword
    }
  }